public class CWH_17_LogicOperator {
    public static void main(String[] args) {
        boolean a=false;
        boolean b=true;
        System.out.println(a && b);
        System.out.println(a || b);
        System.out.println("not of a :" + !a);
        System.out.print("not of b :" + !b);
    }
}
